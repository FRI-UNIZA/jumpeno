class LockToken {
    // Attributes -------------------------------------------------------------------------------------------------------------------------
    #Locked = true; Locked = () => this.#Locked
    #Unlock = () => {}

    // Constructors -----------------------------------------------------------------------------------------------------------------------
    constructor(unlock) {
        this.#Unlock = unlock
    }

    // Actions ----------------------------------------------------------------------------------------------------------------------------
    Unlock() {
        if (this.#Locked) this.#Unlock()
        this.#Locked = false
    }
}
